def main():
    """
    主函数：为每个主题生成训练数据
    """
    topics = {
        "财": ("财_树形结构图.md", generate_wealth_conversations),
        # 其他主题待添加
    }
    
    for topic_name, (md_file, generator_func) in topics.items():
        md_path = Path("e:/SD/bazi/sanbanfu/biao") / md_file
        conversations = generator_func(md_path)
        
        # 保存为JSONL
        output_file = Path("e:/SD/bazi/sanbanfu/biao") / f"{topic_name}_实战对话_training_data.jsonl"
        with open(output_file, 'w', encoding='utf-8') as f:
            for conv in conversations:
                f.write(json.dumps(conv, ensure_ascii=False) + '\\n')
        
        print(f"✅ 生成 {topic_name} 训练数据: {len(conversations)} 条 -> {output_file}")

if __name__ == "__main__":
    main()
