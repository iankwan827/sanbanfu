def generate_conversations(topic_name, scenario_list, response_generator, count=20):
    """
    为指定主题生成训练对话
    
    Args:
        topic_name: 主题名称
        scenario_list: 用户场景列表
        response_generator: 回复生成函数
        count: 生成数量
    """
    conversations = []
    
    # 准备八字样本池
    all_bazi = []
    for bazi_type, charts in BAZI_CHARTS.items():
        for chart in charts:
            all_bazi.append((chart, bazi_type))
    
    # 确保场景不重复
    used_scenarios = set()
    
    for i in range(count):
        # 随机选择八字和场景
        bazi_chart, bazi_type = random.choice(all_bazi)
        
        # 确保场景不重复
        available_scenarios = [s for s in scenario_list if s not in used_scenarios]
        if not available_scenarios:
            used_scenarios.clear()  # 重置
            available_scenarios = scenario_list
        
        user_scenario = random.choice(available_scenarios)
        used_scenarios.add(user_scenario)
        
        # 生成助手回复
        assistant_response = response_generator(bazi_chart, bazi_type, user_scenario)
        
        # 构造对话
        conversation = {
            "messages": [
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user", "content": f"我的八字是: {bazi_chart}\n\n{user_scenario}"},
                {"role": "assistant", "content": assistant_response}
            ]
        }
        
        conversations.append(conversation)
    
    return conversations

