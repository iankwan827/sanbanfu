function generateTrainingData() {
    const data = [];
    const ganList = ['甲', '乙', '丙', '丁', '戊', '己', '庚', '辛', '壬', '癸'];
    const scenarios = [
        { replies: ['对', '准', '是', '有点道理'], template: RESPONSE_TEMPLATES.positive },
        { replies: ['不太对', '不准', '没感觉'], template: RESPONSE_TEMPLATES.negative },
        { replies: ['迷茫', '不知道', '不知道该咋办'], template: RESPONSE_TEMPLATES.confused },
        { replies: ['家里有厂', '家里有矿', '做生意的', '还行吧'], template: RESPONSE_TEMPLATES.wealthy }
    ];

    // 为每个日主生成多种场景
    for (const dm of ganList) {
        // 开场白（3条）
        for (const hook of OPENING_HOOKS[dm]) {
            data.push({
                messages: [
                    { role: 'system', content: '你是八字销冠大师，用性格钩子切入建立信任。说话口语化、带人味，一句话不超过25字。' },
                    { role: 'user', content: `客户日主是${dm}，第一次打招呼` },
                    { role: 'assistant', content: hook }
                ]
            });
        }

        // 各种场景回复
        for (const scenario of scenarios) {
            for (const reply of scenario.replies) {
                const response = scenario.template[Math.floor(Math.random() * scenario.template.length)];
                data.push({
                    messages: [
                        { role: 'system', content: '你是八字销冠大师，根据客户回复引导对话。口语化、简短。' },
                        { role: 'user', content: `日主${dm}，客户说"${reply}"` },
                        { role: 'assistant', content: response }
                    ]
                });
            }
        }

        // 成交引导
        for (const close of RESPONSE_TEMPLATES.close) {
            data.push({
                messages: [
                    { role: 'system', content: '你是八字销冠大师，客户已经信任你了，现在要引导成交。' },
                    { role: 'user', content: `日主${dm}，客户问"那怎么办"` },
                    { role: 'assistant', content: close }
                ]
            });
        }
    }

    return data;
}

// 生成数据
const trainingData = generateTrainingData();
console.log(`生成了 ${trainingData.length} 条训练数据`);

// 保存为 JSONL
const outputPath = path.join(__dirname, 'biao', 'sales_training_data.jsonl');
const jsonl = trainingData.map(d => JSON.stringify(d)).join('\n');
fs.writeFileSync(outputPath, jsonl, 'utf8');
console.log(`已保存到: ${outputPath}`);
